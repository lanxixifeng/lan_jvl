public class bianliang2 {
    public static void main(String[] args)  {
        //定义byte类型变量
        byte b =10;
        System.out.println(b);
        //定义short类型变量
        short s = 11;
        System.out.println(s);
        //定义int类型变量
        int i =12;
        System.out.println(i);
        //定义double类型变量
        double d = 1.3;
        System.out.println(d);
        //定义char类型变量
        char c = 'a';
        System.out.println(c);
        //定义boolean类型变量
        boolean bb = true;
        System.out.println(bb);
        //定义long类型变量(long类型数值任按int类型需要在值的末尾加上 l
        long l = 233333333333l;
        System.out.println(l);
        //定义float类型变量(浮点数默认是double类型需要在值的末尾加上 f
        float f = 13.14f;
        System.out.println(f);
    }
}
/*
long和float数值末尾要加 l 和 f
*/