/*
变量定义格式：
数据类型 变量名 = 变量值；
基本数据类型：
byte，short，int，long,float,double,char,boolean
使用：
取值格式： 变量名
修改值格式：变量名 = 变量值；
*/
public class bianliang {
    public static void main(String[] args) {
        //定义变量
        int a = 123;
        int b = 456;
        //输出变量a
        System.out.println(a);
        //输出变量b
        System.out.println(b);
        //修改变量a
        a = 321;
        System.out.println(a);
        //修改变量b
        b = 654;
        System.out.println(b);
    }
}